/** \brief Devuelve el menu de opciones
 *
 * \return int Retorna la opcion
 *
 */
int menu();



/** \brief Devuelve la suma de dos numeros
 *
 * \param a float Pide el primer numero
 * \param b float Pide el segundo numero
 * \return float  Retorna la  suma
 *
 */
float suma(float a, float b);



/** \brief Devuelve la resta de dos numeros
 *
 * \param a float Pide el primer numero
 * \param b float Pide el segundo numero
 * \return float  Retorna la resta
 *
 */
float resta(float a, float b);



/** \brief  Delvuelve la multiplicacion de dos numeros
 *
 * \param a float Pide el primero numero
 * \param b float Pide el segundo numero
 * \return float  Retorna la multiplicacion
 *
 */
float multiplicar(float a, float b);



/** \brief Delvuelve la division de dos numeros
 *
 * \param a float Pide el primer numero
 * \param b float Pide el segundo numero
 * \return float  Retorna la division
 *
 */
float dividir(float a, float b);



/** \brief Delvuelve el factorial del primer numero
 *
 * \param a float Pide el primero numero
 * \return float  Retorna  al factorial
 *
 */
float factorialA(float a);


/** \brief Delvuelve el factorial del segundo numero
 *
 * \param b float Pide el segundo numero
 * \return float  Retorna al factorial
 *
 */
float factorialB(float b);
